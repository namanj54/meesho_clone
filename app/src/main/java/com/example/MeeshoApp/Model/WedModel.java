package com.example.MeeshoApp.Model;

public class WedModel {

    int iv_wed;
    String tv_wed;

    public WedModel(int iv_wed, String tv_wed) {
        this.iv_wed = iv_wed;
        this.tv_wed = tv_wed;
    }

    public int getIv_wed() {
        return iv_wed;
    }

    public void setIv_wed(int iv_wed) {
        this.iv_wed = iv_wed;
    }

    public String getTv_wed() {
        return tv_wed;
    }

    public void setTv_wed(String tv_wed) {
        this.tv_wed = tv_wed;
    }

}
