package com.example.MeeshoApp.Model;

public class BrandModel {

    int iv_brand;
    String tv_brand;


    public BrandModel(int iv_brand, String tv_brand) {
        this.iv_brand = iv_brand;
        this.tv_brand = tv_brand;
    }

    public int getIv_brand() {
        return iv_brand;
    }

    public void setIv_brand(int iv_brand) {
        this.iv_brand = iv_brand;
    }

    public String getTv_brand() {
        return tv_brand;
    }

    public void setTv_brand(String tv_brand) {
        this.tv_brand = tv_brand;
    }




}
