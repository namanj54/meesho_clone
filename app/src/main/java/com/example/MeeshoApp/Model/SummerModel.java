package com.example.MeeshoApp.Model;

public class SummerModel {

    int iv_sum;
    String tv_sum;

    public SummerModel(int iv_sum, String tv_sum) {
        this.iv_sum = iv_sum;
        this.tv_sum = tv_sum;
    }

    public int getIv_sum() {
        return iv_sum;
    }

    public void setIv_sum(int iv_sum) {
        this.iv_sum = iv_sum;
    }

    public String getTv_sum() {
        return tv_sum;
    }

    public void setTv_sum(String tv_sum) {
        this.tv_sum = tv_sum;
    }



}
